# Array: Uma estrutura de dados que armazena uma coleção de elementos, todos do mesmo tipo, em uma sequência contígua de memória, permitindo acesso eficiente por índice.

# Dicionário: Uma estrutura de dados em Python que armazena pares de chave-valor, permitindo acesso rápido aos valores associados a uma chave específica.

# IPv4: Um protocolo de comunicação usado na Internet que define um endereço IP como um número de 32 bits, geralmente representado em notação decimal pontuada.

# Manipulação de Strings: O processo de alterar, formatar ou analisar cadeias de caracteres em programação, frequentemente usando funções e métodos específicos para strings.

# Número Sortudo: Um conceito em programação onde um número é considerado "sortudo" se sua frequência em um array for igual ao próprio número.

# Ordenação: O processo de organizar elementos em uma sequência específica, como crescente ou decrescente, frequentemente usando algoritmos de ordenação como `sort`.

# Subsequência: Uma sequência derivada de outra sequência removendo alguns elementos sem alterar a ordem dos elementos restantes.

# Tipo de Dados: Uma classificação que especifica quais valores uma variável pode ter e quais operações podem ser realizadas sobre ela, como inteiros, strings e listas.
