# Atributos: Características ou propriedades que são definidas dentro de uma classe e são usadas para armazenar o estado de um objeto. Em Python, são inicializados no método construtor `__init__`.

# Classes: Estruturas que servem como moldes ou modelos para criar objetos com características semelhantes. Elas definem atributos e métodos que os objetos criados a partir delas terão.

# Construtor: Um método especial em uma classe, geralmente chamado `__init__` em Python, que é executado automaticamente quando um novo objeto é criado. Ele é usado para inicializar os atributos do objeto.

# Herança: Um mecanismo que permite que uma classe (subclasse) herde atributos e métodos de outra classe (superclasse), promovendo a reutilização e organização do código.

# Métodos: Funções definidas dentro de uma classe que descrevem os comportamentos que os objetos da classe podem realizar. Eles podem manipular os atributos do objeto e realizar operações.

# Objetos: Instâncias de classes que representam entidades com estado e comportamento definidos pelos atributos e métodos da classe. Cada objeto pode ter valores diferentes para seus atributos.

# Polimorfismo: A capacidade de diferentes classes implementarem métodos com o mesmo nome, mas com comportamentos distintos. Isso permite que o mesmo método seja chamado em diferentes objetos, resultando em ações específicas para cada classe.

# Self: Uma referência à instância atual da classe, usada para acessar atributos e métodos dentro da classe. É um parâmetro obrigatório nos métodos de instância em Python.

# Subclasses: Classes que herdam de outras classes (superclasses), podendo adicionar ou modificar atributos e métodos. Elas promovem a especialização e reutilização de código.

# Superclasses: Classes das quais outras classes (subclasses) herdam atributos e métodos. Elas fornecem uma base comum para as subclasses, promovendo a reutilização de código.
