# CRUD: Acrônimo para Create, Read, Update, Delete, que são as quatro operações básicas de persistência em bancos de dados e manipulação de dados em APIs.

# FastAPI: Um framework moderno e eficiente para a criação de APIs em Python, conhecido por sua alta performance e simplicidade de uso.

# Framework: Uma estrutura de software que fornece uma base sobre a qual programas podem ser desenvolvidos, oferecendo funcionalidades prontas para facilitar o desenvolvimento de aplicações.

# Insomnia: Uma ferramenta de teste de APIs que permite criar e configurar requisições HTTP para verificar o funcionamento de serviços web.

# Poetry: Um gerenciador de dependências para projetos Python, utilizado para instalar e gerenciar pacotes de forma eficiente.

# Requisição HTTP: Uma solicitação enviada por um cliente a um servidor na web, que pode incluir métodos como GET, POST, PUT e DELETE para interagir com recursos.

# Rotas: Caminhos definidos em uma API que determinam como as requisições HTTP são tratadas e quais recursos são acessados.

# Serialização: O processo de converter um objeto em um formato que pode ser facilmente armazenado ou transmitido, como JSON, para ser utilizado em APIs.

# Status HTTP: Códigos que indicam o resultado de uma requisição HTTP, como 200 para sucesso e 404 para recurso não encontrado.

# Teste de API: O processo de verificar se uma API está funcionando corretamente, utilizando ferramentas para enviar requisições e analisar respostas.




# O que é o FastAPI e por que é utilizado no desenvolvimento back-end?: O FastAPI é um framework moderno e eficiente para a criação de APIs em Python. Ele é utilizado no desenvolvimento back-end devido à sua simplicidade, facilidade de aprendizado, alta performance e capacidade de lidar com diversas situações sem falhas. É ideal para aprendizado e uso prático, especialmente em projetos que não são extremamente complexos.


# Quais são as vantagens de usar o FastAPI em comparação com outros frameworks?: O FastAPI oferece várias vantagens, incluindo alta velocidade, simplicidade na criação de rotas, comunicação HTTP, integração com bancos de dados, serialização de informações e implementação de segurança. Ele é altamente performático e pronto para produção, além de ser fácil de aprender e popular entre desenvolvedores.


# Como o FastAPI se compara ao Django?: O FastAPI é mais adequado para projetos que não são extremamente complexos e para desenvolvedores que buscam simplicidade e alta performance. O Django, por outro lado, é recomendado para projetos extremamente complexos devido à sua robustez e recursos abrangentes. A escolha entre os dois depende da experiência do time de desenvolvimento e do tamanho do projeto.


# Quais ferramentas podem ser usadas para testar APIs desenvolvidas com FastAPI?: Ferramentas como o Insomnia podem ser usadas para testar APIs desenvolvidas com FastAPI. Elas permitem criar e configurar requisições HTTP, facilitando o teste e a validação das funcionalidades da API.


# Por que é importante entender os métodos HTTP e seus códigos de status?: Entender os métodos HTTP e seus códigos de status é importante porque eles indicam o resultado das requisições feitas à API. Conhecer os códigos mais recorrentes, como 200 (sucesso) e 404 (recurso não encontrado), ajuda a compreender a lógica por trás das interações com a API e a diagnosticar problemas.