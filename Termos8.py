# API Gateway: Um serviço que atua como um ponto de entrada para um conjunto de APIs, gerenciando o tráfego de entrada, autenticação, autorização e roteamento de requisições para os serviços apropriados.

# Autenticação: O processo de verificar a identidade de um usuário ou sistema, geralmente através de credenciais como nome de usuário e senha, ou tokens de acesso.

# Autorização: O processo de determinar se um usuário ou sistema autenticado tem permissão para acessar ou executar uma ação em um recurso específico.

# CI/CD: Abreviação de Continuous Integration/Continuous Deployment, refere-se a práticas de desenvolvimento de software que permitem a integração e entrega contínua de código, facilitando a automação de testes e implementações.

# FastAPI: Um framework web moderno e de alto desempenho para construir APIs com Python, baseado em padrões como OpenAPI e JSON Schema, que facilita a criação de aplicações rápidas e eficientes.

# JWT (JSON Web Token): Um padrão aberto para criar tokens de acesso que permitem a troca segura de informações entre partes, usado frequentemente em autenticação e autorização.

# OAuth 2.0: Um protocolo de autorização que permite que aplicativos acessem recursos de usuário em um servidor de forma segura, sem expor as credenciais do usuário.

# Pydantic: Uma biblioteca de validação de dados para Python que utiliza tipagem avançada para garantir que os dados sejam validados e serializados de forma eficiente e intuitiva.

# Swagger: Uma ferramenta para documentação de APIs que permite a criação de documentações interativas e compreensíveis, facilitando o uso e entendimento das APIs por desenvolvedores.

# Tipagem Avançada: O uso de tipos de dados complexos e específicos para garantir a integridade e a validação dos dados em aplicações, especialmente em linguagens de programação que suportam tipagem estática ou dinâmica.




# O que é escalabilidade e por que é importante?: Escalabilidade refere-se à capacidade de um sistema de lidar com um aumento na carga de trabalho ou de se expandir para acomodar esse aumento. É importante porque garante que o sistema possa crescer e se adaptar a novas demandas sem comprometer o desempenho ou a funcionalidade.


# Como o Pydantic é utilizado?: O Pydantic é utilizado para tipagem avançada, validação e serialização de dados de forma intuitiva. Ele facilita a estruturação de informações e garante que os dados manipulados sejam consistentes e seguros.


# Qual é a importância da documentação no desenvolvimento de APIs?: A documentação é essencial no desenvolvimento de APIs, pois garante que elas sejam compreensíveis e fáceis de usar. O módulo recomenda o uso do Swagger para criar documentações avançadas, facilitando a comunicação e integração entre diferentes partes de um sistema.


# Qual é a diferença entre autenticação e autorização?: Autenticação é o processo de verificar a identidade de um usuário, enquanto autorização determina o que um usuário autenticado pode fazer dentro do sistema. Ambos são cruciais para a segurança e funcionalidade dos sistemas.


# Quais métodos de autenticação são discutidos no módulo?: O módulo discute vários métodos de autenticação, incluindo usuário e senha, JWT (JSON Web Tokens), API Gateway e OAuth 2.0, preparando os alunos para lidar com essas tecnologias em suas carreiras.


# Como o FastAPI é utilizado no projeto contínuo do módulo?: O FastAPI é utilizado para implementar e testar rotas, destacando a importância de enviar dados no corpo da requisição em formato JSON. Ele facilita a criação de APIs rápidas e eficientes, integrando-se bem com outras ferramentas discutidas no módulo.


# Por que é importante entender referências e estruturas de dados?: Entender referências e estruturas de dados é crucial para manipular dados de forma eficiente e evitar problemas comuns, como manipulação incorreta de dicionários. Isso garante que o sistema funcione corretamente e de forma otimizada.


# O que é abordado na configuração do ambiente de desenvolvimento?: A configuração do ambiente de desenvolvimento inclui a criação de um repositório no GitHub, a adição de dependências usando o Poetry e a configuração do projeto para usar o FastAPI. Isso prepara os alunos para trabalhar em projetos reais e colaborativos.
