# Array = Lista

gato = 10
peixe = 15
cachorro = 30
dinossauro = 500

minha_listinha = []
minha_listinha.append(gato)
minha_listinha.append(peixe)
minha_listinha.append(cachorro)
minha_listinha.append(dinossauro)
# append é um super-poder, é uma ferramneta que eu uso para colocar o valor no meu Array/Lista

# Dicionário = HashMap

meu_dicionario = {
    "gato": 10,
    "peixe": 20,
    "cachorro": 30,
    "dinossauro": 500
}

print(meu_dicionario["gato"])

# Lista uma estrutura sequencial. Dicionario uma estrutura de chave e valor